def add_two_numbers(num_one, num_two):
    total = num_one + num_two
    print(total)

def add_three_numbers(num_one, num_two, num_three):
    result = num_one + num_two + num_three
    print(result)

def add_four(num_one, num_two, num_three, num_four):
    adicao = num_one + num_two + num_three + num_four
    print(adicao)

add_two_numbers(2, 3)
